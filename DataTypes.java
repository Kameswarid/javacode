package basics;

public class DataTypes {

	static void updatevariable()
	{
		int a=10;
		System.out.println(a);
		a=100;
		System.out.println(a);
	}
    static void datatypes()
    {
    	byte a=101;
    	float b=101.5f;
    	float c=a+b;
    	System.out.println(c);
    	
    	String name ="Ram";
    	name="Ram krishna";
    	System.out.println(name);
    	
    	char gender='M';
    	System.out.println(gender);
    	
    }
public static void main(String[] args)
{
	updatevariable();
	datatypes();
}
}

